/* 常用正则表达式*/
export default {
    IDcard: /^\d{6}(19|2\d)?\d{2}(0[1-9]|1[012])(0[1-9]|[12]\d|3[01])\d{3}(\d|X)?$/,
    cell_phone: /^(1[3-9][0-9])\d{8}$|\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/,
    mobile: /^((13|14|15|17|18)[0-9]{1}\d{8})$/,
    chinese: /^[\u0391-\uFFE5]+$/,
    date: /^\d{4}-\d{2}-\d{2}$/,
    digits: /^\d+$/,
    email: /^\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/,
    letters: /^[a-z]+$/i,
    person_name: /^(?!\d+$)[\dA-Za-z\u2E80-\uFE4F]{1,20}$/,
    qq: /^[1-9]\d{4,}$/,
    tel: /^(?:(?:0\d{2,3}[\- ]?[1-9]\d{6,7})|(?:[48]00[\- ]?[1-9]\d{6}))$/,
    time: /^([01]\d|2[0-3])(:[0-5]\d){1,2}$/,
    url: /^(https?|s?ftp):\/\/\S+$/i,
    zipcode: /^\d{6}$/,
    telPhone: /^((13[0-9])|(14[5|7])|(15([0-3]|[5-9]))|(18[0,5-9]))\d{8}$|^((13[0-9])|(14[5|7])|(15([0-3]|[5-9]))|(18[0,5-9]))\d{8}$|^0\d{2,3}-?\d{7,8}$/,
    longitude: /^[\-\+]?(((\d|[1-9]\d|1[0-7]\d|0)\.\d{0,10})|(\d|[1-9]\d|1[0-7]\d|0{1,3})|180\.0{0,10}|180)$/,
    latitude: /^[\-\+]?([0-8]?\d{1}\.\d{0,10}|90\.0{0,10}|[0-8]?\d{1}|90)$/,
    zmszhz: /^[A-Za-z0-9\u4e00-\u9fa5]+$/, // 数字、字母、汉字
    price: /^[0-9]+([.]{1}[0-9]{1,2})?$/,  // 数字（最多保留2位小数）
    priceDecimal: /^[0-9]+([.]{1}[0-9]{1,4})?$/, // 数字（最多保留4位小数）
    money: /^\d+(\.\d{1,2})?$/, // 正数 最多保留两位小数
    money2: /^[\-\+]?\d+(\.\d{1,2})?$/, // 正数或负数 最多保留两位小数
    money3: /^\d{1,10}(\.\d{1,2})?$/,
    discount: /^(\d|[1-9]\d|100)(\.\d{1,2})?$/, // 0-100之间的值  可保留2位小数
    passWord: /^[a-zA-Z0-9\x21-\x7e]{6,20}$/		// 6-20位字符；数字、字母、特殊字符（除空格）
}
